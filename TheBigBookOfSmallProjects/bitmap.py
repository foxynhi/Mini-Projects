print('Enter the message to display with the bitmap.')
while True:
    message = input('> ')
    if message != '':
        break

l = len(message)
for line in bitmap.splitlines():
    blank = 0
    for n, char in enumerate(line):
        if char == ' ':
            print(' ', end='')
            blank += 1
        else:
            print(message[(n - blank) % l], end='')

    print()